clc;
clear all;
A = [1,1;1,2;1,4;1,8;1,12;1,15;1,19;1,23;1,27;1,29;1,30;1,32;1,33];
b = [log10(2250);log10(2500);log10(5000);log10(29000);log10(120000);log10(275000);
    log10(1180000);log10(3100000);log10(7500000);log10(24000000);log10(42000000);
    log10(220000000);log10(410000000)];
theta = lsqr(A,b);
n= rms(A*theta-b);
x = [1971 1972 1974 1978 1982 1985 1989 1993 1997 1999 2000 2002 2003];
y = [2250 2500 5000 29000 120000 275000 1180000 3100000 7500000 24000000 42000000 220000000 410000000];
semilogy(x,y,'ro');
hold on;
line = 10.^(theta(1) + theta(2)*(x-1970));
plot(x, line,'blue');
xlabel("Year");
ylabel("Number of Transistor");
legend("Data Point","Linear Regression Line");
